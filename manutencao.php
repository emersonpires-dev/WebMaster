                <h2>Nossos Planos de Suporte</h2>
                <p>Entendemos que cada negócio tem uma necessidade diferente. Por isso, oferecemos diferentes modalidades de suporte:</p>
                <ul>
                    <li><strong>Suporte Corretivo:</strong> Atuação rápida para corrigir falhas e bugs que impactam a operação. Disponível em regime 8/5 ou 24/7, com SLAs (Acordos de Nível de Serviço) bem definidos.</li>
                    <li><strong>Manutenção Preventiva:</strong> Ações proativas como monitoramento de logs, otimização de banco de dados, verificação de segurança e aplicação de patches para evitar problemas futuros.</li>
                    <li><strong>Suporte Evolutivo:</strong> Trabalhamos em um backlog contínuo de melhorias, implementando novas funcionalidades e otimizando as existentes para que seu sistema acompanhe as mudanças do mercado.</li>
                    <li><strong>Suporte Técnico a Usuários:</strong> Oferecemos canais de atendimento para auxiliar os usuários do seu sistema, tirando dúvidas e resolvendo problemas de utilização (Nível 1 e Nível 2).</li>
                </ul>

